		<h2>Première phase: <a href="/echanger">les cercles citoyens</a></h2>
		<p>Le Cahier de participation sera le principal instrument de la première phase des débats autour de ce nouvel enjeu. Ce cahier sera accompagné d'un cahier de formation et d'un lexique afin de faciliter la participation des membres et aussi des non-membres aux débats.</p>

		<p>Notre Déclaration de principes dit en effet que : « <em>La politique doit être repensée dans le sens d’une démocratie participative qui doit être exercée dans les villes, villages, quartiers, ainsi que dans toutes les instances de notre parti. Les solutions aux problèmes émergeront des espaces de délibération encourageant la participation de toutes et de tous.</em> » Dès lors, la discussion intègre non seulement les membres du parti, mais aussi, par le biais des cercles citoyens, l'ensemble des citoyens-nes qui veulent y participer.</p>

		<p>Ces cercles citoyens sont composés d’au moins trois personnes dont une doit être membre de Québec solidaire. Les autres personnes peuvent être des amis-es, des collègues de travail, la famille ou encore des militants-es d’un groupe. Les cercles se rencontrent pour discuter des questions et des pistes de réflexion soulevées dans par les quatre thèmes de l'enjeu:</p>

		<? Module::DisplayNewModule('themes') ?>

		<p>Fait nouveau, pour chacune de ces questions, plusieurs pistes de réflexion sont souvent proposées dans ce cahier. <b>Important : ce ne sont toutefois que des alternatives possibles. Elles ne sont donc ni exhaustives, ni nécessairement mutuellement exclusives. Toutes vos idées et propositions sont les bienvenues et contribueront à enrichir le débat.</b></p>

		<p>Vous n’avez pas à aborder toutes ces questions, vous pouvez faire un choix selon vos champs d’intérêt et vous pouvez aussi en aborder de nouvelles.</p>

		<p>Deuxième fait nouveau, nous acceptons aussi cette année les contributions individuelles. Nous vous encourageons cependant à créer des cercles citoyens, le partage de réflexion étant toujours très constructif.</p>
	
		<p>Vous pouvez contribuer de trois façons : 
<ul>
	<li>En remplissant le <a href="/contribuer">formulaire en ligne</a>
	<li>En nous envoyant par la poste le formulaire du cashier de participation (que vous pouvez télécharger <a href="/reflechir">ici</a>). </li>
	<li>En envoyant la contribution par <a href="mailto:programme@quebecsolidaire.net">courriel</a></li>.
</ul>
	<p>Les associations de Québec solidaire dans les différentes régions du Québec peuvent aussi vous aider à produire des contributions. Pour contacter l’association de votre circonscription ou de votre région, consultez le site de <a href="http://quebecsolidaire.net">Québec solidaire</a> ou téléphonez au 1-800-278-9014.</p>

	<div class="bloc-eche">
		<h2>Deuxième phase: Cahier de perspectives, conférences thématiques et camp de formation</h2>
		<p>La deuxième phase débutera par la tenue d’un camp de formation portant sur le Cahier de perspectives, au cours de l’automne prochain. Ce dernier présentera les principales orientations qui se seront dégagées des contributions. Quatre conférences thématiques auront aussi lieu lors de ce camp de formation pour alimenter les discussions et présenter les débats sur les différents enjeux. </p>
	</div>
	<div class="bloc-eche">
		<h2>Troisième phase: Assemblées générales, Cahier synthèse et Congrès</h2>
		<p>Finalement, les membres de Québec solidaire seront appelés à formuler des amendements et des nouvelles propositions au Cahier de perspectives en participant aux activités de leur instance.</p>

		<p>Une synthèse de cet exercice sera présentée dans un « Cahier synthèse ». Les membres auront aussi à se prononcer sur ce cahier lors d’assemblées générales et à nommer des délégués-es en vue du Congrès d’orientation.</p>
		
	</div>
	<div class="bloc-eche">
		<h2>Des personnes-ressources disponibles pour les associations</h2>
		<p>La Commission politique met à la disposition des associations des personnes-ressources pour les différents thèmes de l’enjeu 2. Ces personnes peuvent répondre à vos questions par <a href="mailto:programme@quebecsolidaire.net">courriel</a>, par téléphone et même participer à vos rencontres si vous le jugez nécessaire. Vous trouverez leurs coordonnées sur le site du programme.</p>
	</div>
